#include "printf.h"

int	ft_isnumber(char *s)
{
	int n;

	if (s == NULL)
		return (0);
	if (*s == '-')
		s++;
	n = 0;
	while (*s != '\0')
	{
		if (!ft_isdigit(*s))
			return (0);
		s++;
		n++;
	}
	if (n == 0)
		return (0);
	return (1);
}
