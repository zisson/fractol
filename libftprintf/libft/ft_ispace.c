#include "includes/libft.h"

int	ft_ispace(int c)
{
	if (c == '\n' || c == '\t' || c == '\a' || c == '\b' || c == '\v'
		|| c == '\f' || c == '\r' || c == ' ')
		return (1);
	return (0);
}
